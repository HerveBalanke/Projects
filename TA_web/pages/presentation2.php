                        <h2 class="text-center"> <b>NOTRE CONFESSION DE FOI </b></h2>
                <p>Ci-dessous sont les principes de notre foi, en d’autres mots, ce en quoi nous croyons.</p>
                <h3><b>Les Saintes-Ecritures</b></h3>
                <p>Nous croyons que la bible est la parole infaillible inspirée de Dieu. La nouvelle alliance comme mentionnée dans le nouveau testament est notre guide dans les questions relatives à la 
                  conduite et à la doctrine (<b>2Timothee 3:16; 1 Thessaloniciens 2:13; 2Pierre 1:21</b>)</p>
                  <h3><b>Dieu</b></h3>
                <p>Notre Dieu est un, mais se manifeste en trois personnes appelé Père, Fils et Saint-Esprit étant tous égaux (<b>1Jean 5:7</b>)</p>
                <p>Dieu le Père est l’envoyeur de la parole (<b>Logos</b>) et le créateur (<b>Jean 14:28; Jean 16:28; Jean 1:14</b>)</p>
                <p>Le Fils est la parole faite chair, l’unique qui a existé avec le Père au commencement (<b>1Timothee 2:5; Jean 1:14; Jean 1:18; Jean 1:1</b>). Il est véritable Dieu et homme, il a été conçu du Saint-Esprit 
                  et né d’une vierge appelée Marie. Il a été crucifie, est mort et est ressuscité le 3eme jour, il est monté au ciel et est assis à la droite du Père comme notre souverain sacrificateur et avocat 
                  (<b>Hébreux 7:24-25; Romains 8:34; 1Jean 2:1</b>)</p>
                <p>Le Saint-Esprit vient du Père et du Fils et est éternel (<b>Jean 15:26</b>)</p>
                <h3><b>L’Homme, sa Chute et la Redemption</b></h3>
                <p>L’homme est une créature fait à l’image et à la ressemblance de Dieu. Mais au travers d’Adam la transgression et la chute, le péché sont entrés dans le monde. Jésus-Christ, le fils de Dieu a paru afin 
                  de détruire les œuvres du diable et il a donné sa vie en rançon pour sauver et racheter l’homme. (<b>Romains 3:23; Romains 3:10; 1Jean 3:8</b>). Le salut est le don de Dieu à l’humanité et est opérationnel par 
                  la grâce par le moyen de la foi en Jésus-Christ, produisant de bonne œuvre acceptable devant Dieu.</p>
               <h3><b>La Vie Eternelle et la Nouvelle Naissance</b></h3>
                <p>Nous croyons que le premier pas de l’homme vers le salut est la repentance. La nouvelle naissance est nécessaire pour tous les hommes et quand on en fait l’expérience, elle produit la vie éternelle 
                  (<b>Jean 3:3-5; 1Jean 5:12</b>)</p>

                  <h3><b>L’Eglise</b></h3>
                <p>La véritable église est composée de tous ceux qui sont nés de nouveau. Au travers de cette nouvelle naissance nous sommes unis ensemble dans le corps de Christ. Jésus-Christ est le seigneur et la tête de l’église. 
                  Chaque a le droit sous l’autorité de Christ de diriger et gouverner ses propres affaires (<b>Ephésiens 4:15-16</b>)</p>

                  <h3><b>Le Bapteme d’Eau</b></h3>
                <p>Nous croyons au baptême par immersion est un commandement direct de notre Seigneur, et c’est uniquement pour les croyants. C’est un symbole de l’identification du croyant avec Christ dans sa mort, son ensevelissement
                 et sa résurrection (<b>Matthieu 28:19; Romains 6:4; Colossiens 2:12; Actes 8:36-37</b>)</p>

                 <h3><b>Le Bapteme Dans le Saint-Esprit</b></h3>
                <p>Nous croyons au baptême dans le Saint-Esprit est un don de Dieu comme promis par le Seigneur Jésus-Christ a tous les croyants et est reçu consécutivement a la nouvelle naissance. Cette expérience est accompagnée par le parler 
                  en langue selon que le Saint-Esprit donne de s’exprimer (<b>Matthieu 3:11; Jean 14:16-17; Actes 1:8; Actes 2:38-39; Actes 19:17; Actes 2:4</b>)</p>
                <h3><b>La Sanctification</b></h3>
                <p>Nous croyons que la bible enseigne la sanctification, sans laquelle aucun homme ne verra Dieu. Nous croyons en la doctrine de la sanctification en tant que œuvre achevée de la grâce, mais progressive, qui commence au moment de la 
                  nouvelle naissance et se poursuit jusqu’à atteindre la ressemblance parfaite a Jésus-Christ (<b>Hébreux 12:14; 1Thessaloniciens 5:23; 2Pierre 3:18; 2Corinthiens 3:18; Philippiens 3:12-14; 1Corinthiens 1:20</b>)</p>
                <h3><b>Les Miracles, Les Signes et les Prodiges</b></h3>
                <p>Nous croyons aux miracles qui sont une expression de la puissance de Dieu. Par un aperçu du nouveau testament (Matthieu 4:11; 1Corinthiens 2:10) nous voyons que les miracles peuvent être provoques par des mystères tels que:</p>
                <ul style="list-style-type:circle">
                <li>L’imposition des mains (<b>2Timothee 6:6; Deutéronome 34:9</b>)</li>
                <li>La sainte cène (<b>1Corinthiens 11:23-24</b>)</li>
                <li>L’onction d’huile sur des personnes ou des choses (<b>Marc 6:13; Exode 30:25-31</b>)</li>
                <li>Le lavement des pieds (<b>Jean 13:3-10</b>)</li>
                <li>Le sang de l’aspersion (<b>1Corinthiens 5:7; Exode 12:25</b>)</li>
                <li>Déclaration prophétique (<b>Esaïe 44:26; Matthieu 11:23</b>)</li>
                </ul>
                <h3><b>La Guérison Divine</b></h3>
                <p>Nous croyons que la guérison divine est pour les maladies physiques et spirituelles. Elle est opérée par la puissance de Dieu au travers de la prière de foi et par l’imposition des mains. Elle est rendue disponible par le sacrifice de 
                  Christ et est accessible à tous les enfants de Dieu. (<b>Jacques 5:14-15; Marc 16:18; Esaïe 53: 4-5; Matthieu 8:17; 1Pierre 2:24</b>)</p>
                <h3><b>La Résurrection des Justes et le Retour de Notre Seigneur</b></h3>
                <p>Nous croyons en la seconde venue de Christ et a l’enlèvement de son église. L’ange a dit aux disciples de Jésus « ce même Jésus qui a été enlevé au ciel reviendra de la même manière, comme vous l’avez vu monter au ciel,… les mort en Christ 
                ressusciteront les premiers. Ensuite, nous qui sommes vivants, seront enlevés ensembles avec eux dans les nuées pour rencontrer le Seigneur dans les airs » (<b>Actes 1:11; 1Thessaloniciens 4:16-17</b>)</p>
               <h3><b>L’Enfers et la Rétribution Eternelle</b></h3>
                <p>L’enfer est réel et a été préparé initialement pour Satan et les anges déchus. Celui qui meurt dans ses péchés sans recevoir Jésus-Christ est perdu a jamais et par conséquent n’a plus d’espoir. Le lac de feu éternel l’attend après le jugement 
                  dernier (<b>Hébreux 9:27; Apocalypse 19:20</b>) </p>
